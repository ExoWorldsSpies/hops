def gauss_numerical_integration(f, x1, x2, precision, *f_args):

    x1, x2 = (x2 - x1) / 2, (x2 + x1) / 2

    return x1 * np.sum(gauss_table[precision][0][:, None] *
                       f(x1[None, :] * gauss_table[precision][1][:, None] + x2[None, :], *f_args), 0)


